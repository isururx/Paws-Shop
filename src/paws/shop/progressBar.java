    public void filltaskbar () {
    Timer timer = new Timer(50, new ActionListener() {
        int progressValue = 0; // Start the progress value at 0

        @Override
        public void actionPerformed(ActionEvent evt) {
            progressValue += 2.5;  // Increment by 2.5 every 50 ms
            jProgressBar1.setValue((int) progressValue);  // Set progress value

            // Stop the timer when progress reaches or exceeds 100
            if (progressValue >= 100) {
                ((Timer) evt.getSource()).stop();
                endTaskbar();
            }
        }
    });

    timer.start(); // Start the timer to begin filling the progress bar
}
    public void endTaskbar(){
        this.dispose();
        JOptionPane.showMessageDialog(this, "Email was sent.");
    }

    
